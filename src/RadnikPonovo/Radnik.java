package RadnikPonovo;

public class Radnik {
    public long jmbg;
    public String imePrezime;
    public int brojDece;
    public int stepenStrucneSpreme;
    public int radniStaz;

    public Radnik(long jmbg, String imePrezime, int brojDece, int stepenStrucneSpreme, int radniStaz) {
        this.jmbg = jmbg;
        this.imePrezime = imePrezime;
        this.brojDece = brojDece;
        this.stepenStrucneSpreme = stepenStrucneSpreme;
        this.radniStaz = radniStaz;
    }
}
